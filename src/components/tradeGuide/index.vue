                  <div class="details">
                    <span class="goBack"><i class="iconfont">&#xe604;</i></span>
                    <p>支撑线和阻挡线是技术性分析最基本且必不可少的成分。支撑区和阻挡区是发生大量交易以及存在相当大的购买或出售压力的价格区域。潜在支撑(购买压力)让市场处于上升趋势，间接阻挡(出售压力)让市场处于下跌趋势。一旦交易者能准确判定它们的水平线在何处，便可非常有效地利用它们控制风险并确定盈利的时机。在很可能发生重大变动的价格水平线上进入交易，此超越风险或者报酬超过风险的可能性将得到提高。支撑和阻挡水平可适用于各个交易者的期限。观察市场在遇到这些水平时的反应是估测潜在趋势稳定性的重要指标。它们也是中断涨跌的关键点。大量停损单通常将在关键支撑和阻挡区域累积，并且一旦这些区域被渗透，通常将会有助于市场价格在中断的方向上发生戏剧性上涨。</p>
                    <p><b>支撑线</b></p>
                    <p>支撑区是对产品需求应增长的价格区域。从图表中很容易找到支撑区。当市场处于上升趋势，任何先前已建立的聚积区域处于上升态势，通常是支撑区。要绘出支撑线，则至少需要在图表上找到两个遵循此标准的点。那就会构成一条可在图表内延长的线。</p>
                    <p>当底侧的支撑区域被渗透，那么可成为距离后面上涨最近的阻挡区。</p>
                    <p><img src="../../assets/images/zhi1.png" class="img_zoom"></p>
                    <p><b>阻挡线</b></p>
                    <p>阻挡线是指以增长的出售发行压力或者增长的某一特定投资产品的供应为特征并往往使提升变得平稳的的价格区域。若市场处于上升趋势，所达到的新的高价点或者上端的聚积将产生阻挡区。要绘出一条阻挡线，则至少需要从图表上找到两个符合此标准的点。那么这就构成了一条可在图表上延长的线。 当上部的阻挡区被渗透，那么可成为距离后面下降最近的支撑区域。</p>
