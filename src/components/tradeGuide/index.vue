                <li>
                  <p class="listP">
                    <span>技术性分析概述</span>
                    <i class="iconfont">&#xe604;</i>
                  </p>
                  <div class="details">
                    <span class="goBack"><i class="iconfont">&#xe604;</i></span>
                    <p>技术分析是以预测价格未来走势为目的，以图表形态、技术指标等为手段，对市场展开包括归纳、分析、排除、确认、比较、决策、验证等一系列的思维和研究。</p>
                    <p>技术分析的基本观点是：所有汇价的实际供需量及其背后起引导作用的种种因素，包括投资市场上每个人对未来的希望、担心、恐惧、猜测等，都集中反映在外汇的价格和交易量上，因而研究它们是最直接有效的。</p>
                    <p>技术分析是相对于基本面分析而言的。基本面分析法着重于对政局政策、经济情况、市场动态等因素进行分析，以此来研究外汇的价格是否合理；而技术分析则是透过图表或技术指标的记录，研究市场过去及现在的行为反应，以推测未来价格的变动趋势，其依据的技术指标是由汇价涨跌或成交量等数据计算而出的。技术分析只关心外汇市场本身的变化，而不考虑会对其产生某种影响的经济、政治等各种外部因素。</p>
                    <p>基本面分析的目的是为了判断汇价现行的价位是否合理并描绘出它长远的发展空间，而技术分析主要是预测短期内汇价涨跌的趋势。通过基本面分析我们可以了解应购买何种货币对，而技术分析则让我们把握具体购买的时机。在时间上，技术分析法注重短期分析，在预测旧趋势结束和新趋势开始方面优于基本面分析法，但在预测较长期趋势方面则不如基本面分析法。</p>
                    <p>技术分析和基本面分析都认为汇价是由供求关系所决定，但是基本面分析主要是根据对影响供需关系种种因素的分析来预测汇价走势，而技术分析则是根据汇价本身的变化来预测汇价走势；基本面分析研究市场运动的原因，而技术分析研究市场运动的效果。但是人们更关心的是如何预测汇价的未来趋势以及买卖汇价的适当时机，因而对技术分析情有独衷。</p>
                    <p>技术分析基于以下三个基本前提：</p>
                    <p><b>1、历史自我重复：</b></p>
                    <p>技术分析主体以及市场行为研究的大部分都与人类哲学研究有关。例如，在过去100多年里被辨识并加以分类的图表模式，反映了出现在价格图表上的某些标准图景。这些图景展现出市场牛市或熊市的心理状态。由于这些模式在过去运作良好，我们假定它们在将来也会继续如此。</p>
                    <p>表达这个前提的另一个方法是理解未来立基于对过去的研究这一关键，或者这个将来只是过去的重复。</p>
                    <p><b>2、价格活动是有趋向性的</b></p>
                    <p>趋向的概念对于技术分析来说至关重要。此处仍然如此：除非一个人接受市场实际上是存在趋向性的前提，否则就没有必要继续读下去了。将市场的价格行为制表的全部目的，就是为了辨识其发展早期的趋向，并以此趋向为依据。</p>
                    <p>此外，认识到处于运动中的趋向比颠倒的趋向更易持续是非常重要的。一个趋向在其改变方向之前会一直持续。这听起来是一个显而易见的概念，但这正是我们试图实现的。我们在寻找一个市场最有可能的活动。如果市场上升，则它在反转之前就会一直上升。如果我们能辨识出市场正在上升，那么我们就会买入该产品，直至这种辨识告诉我们其它情况。</p>
                    <p><b>3、市场行为将所有事物打了折扣</b></p>
                    <p>这个陈述或许构成了技术分析的基石。除非您能理解和接受这个前提，否则实际上一切都没有意义。技术分析家相信一切可以影响价格的因素--基本面，政治，心理因素或其它--已经由该产品的价格反映出来了。因此可以推断出，分析公司收益预报是没有用处的，因为市场已经用股票价值为它定价了。因此，所有需要做的就是研究价格行为。虽然这个主张出现伊始会显得捕风捉影和难以置信，但如果一个人花时间去思考它真正的含义，就很难提出异议。</p>
                    <p>通常，图表制作分析家本身并不关心价格上涨或下跌的原因。在某个价格趋向早期或关键的转折点，往往似乎无人确切知晓为什么市场会以某种形式进行。这对于图表制作分析家而言无关紧要，因为他会注意跟随这个趋向。他知道市场上涨或下跌必有其原因，但他就是不相信了解那些原因是什么对于预测价格有必要。</p>
                    <p>由此得出结论，如果影响市场价格的一切事物最终在市场上被反映出来，那么唯一必要的事情就是研究价格行为，而不是它为什么会变动。通过研究价格图表和许多其它技术指标，图表制作分析家实际上得知了市场更可能走向何处。</p>
                    <p>讨论技术分析时，另一件必须谨记在心的重要事情是它有自我实践的习惯。由数百万计的交易者把技术分析作为信仰。当图表模式暗示他们应该买入时，他们就会买入。如果它指示他们应该卖出，他们就会卖出。如果数百万人都遵循同样的模式，价格就会分毫不差地按照他们想的那样活动，因为他们都是这样做的。如果每个人都看了图表，然后认为在某一点市场会上涨，他们都会在这一点买入。由于他们在这一点都买入了，市场就会上涨，因为该产品的需求因素大于供给。有一百万个技术分析家在这一点都买入了。我们也不妨加入他们。</p>
                  </div>
