                    <p><b>限价盘</b></p>
                    <p>限价盘指的是客户希望以当前优于市价的价格买入或卖出某种产品。但必须按照与当前市场价格差距最小的价格下单。交易中心将会明示该差价，因此您会知晓如何建立限价盘。当市场达到所要求的价位，会自动为客户执行这笔交易。这些下单的目的是获得比当前市场更好的价格。只要价格与市场价的差距大于最小差价，客户就可随时根据所需取消和修改这些限价盘。</p>
                    <p>例如，当前USD/JPY市场价为105.90，客户可以下单，以105.00的价格买入5 USD/JPY。</p>
                    <p><b>止损盘</b></p>
                    <p>止损盘用于约束和限制未平盘合约损失。通常，如果市场活动与客户未平盘合约反向，则在新盘后设立止损以限制损失。如果市场向有利于客户的方向活动，止损盘可以随时轻松修改以锁定任何收益。但是，这些盘的要点在于如果出现与客户所持盘反向的突发性市场活动，它会保护客户免遭严重损失。只要价格与市场价的差距大于最小差价，客户就可随时根据所需取消和修改止损盘。</p>
                    <p>例如，当前GBP/USD市场价为1.8140，客户可以下单，以1.8050的价格卖出1 GBP/USD。</p>
                    <p><b>对冲</b></p>
                    <p>这是具有止损价格和止盈价格的双价盘。如果执行其中一个，另一个被就会被取消。这就确保了客户对于市场活动正向或反向有备无患。如果市场活动与客户预期反向，则避免大额损失；如果市场活动与其预期相同，则确保利润。</p>
