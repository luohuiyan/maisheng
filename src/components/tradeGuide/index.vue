                  <p class="listP">
                    <span>移动平均线</span>
                    <i class="iconfont">&#xe604;</i>
                  </p>
                  <div class="details">
                    <span class="goBack"><i class="iconfont">&#xe604;</i></span>
                    <p>利用移动平均线很可能会比其它技术性指标合起来促成的交易金额更多。它们易于计算，并可用于寻找长期的、以月为单位的发展趋势，规定日交易的停止点，以及判定市场中固定的进出点。移动平均线理顺市场波动以及短期起伏，便于交易者了解市场的发展趋势。对于交易者而言，它本身是一个非常有用的工具，但事实上它们还能提供更多的信息。</p>
                    <p>移动平均线是显示一段时期内股票价格平均价值的指标。当计算移动平均线时，应对预先确定的时期内的证券平均价值进行精确分析。当计算移动平均线时，需制定时间范围来计算平均价格(例如，二十五天)。证券价格变化时，其平均价格也将有所涨跌。 把证券在最近"n"个单位的时间内的价格相加，然后用"n"除尽。例如，把最近25天内证券的收盘价格相加，然后用25除尽。其结果就是证券在过去25天的平均价格。图表中每个时间段都将作此计算。 </p>
                    <p><b>解释说明</b></p>
                    <p>解释移动平均线最常用的方法就是比较证券价格移动平均线与证券自身价格的关系。当证券价格上涨，高于其移动平均线，则产生购买信号。当证券价格下跌，低于其移动平均线，则产生出售信号。</p>
                    <p>之所以产生此信号，是因为人们认为，移动平均线"线"是支撑或阻挡价格的有力标准。价格应自移动平均线反弹。若未反弹而突破，那么它应继续在该方向上发展，直至其找到能够保持的新水平面。</p>
                    <p><img src="../../assets/images/yi1.png" class="img_zoom"></p>
                    <p>短期移动平均线往往产生更多购买/出售信号。但是它们中有些是错误的。</p>
                    <p><img src="../../assets/images/yi2.png" class="img_zoom"></p>
                    <p>长期移动平均线往往更可靠，并产生为数甚少的进入信号。但是，由于变动正在进行中，在产生信号之前，变动的实质性部分也许会错过。</p>
                  </div>
