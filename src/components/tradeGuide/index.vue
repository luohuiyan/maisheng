                    <p>外汇保证金交易就是投资者以银行或经纪商提供的信托进行外汇交易。它充分利用了杠杆投资的原理，在金融机构之间及金融机构与投资者之间进行的一种远期外汇买卖方式。在交易中投资者只需支付一定的保证金就可以进行100%额度的交易，使得那些拥有少额资金的投资者也能参与到金融市场上进行外汇交易。按照国外发达国家的水平来看，一般的融资比例维持在10-20倍以上。换言之如果融资比例在20倍，那么投资者只要需要支付5%左右的保证金就能够进行外汇交易了。即投资者只需要支付5000美元就能进行100,000美元的交易。</p>
                    <p>保证金是给予投资者权利买入或卖出某种投资产品的优先合约价值的诚意保证金。例如，凭借1000美元的保证金，一个投资者能够获得价值100000美元的投资产品的贷款。</p>
                    <p>这里有一个简单的例子：</p>
                    <p>一个投资者想要购买一块价值1000000美元的房地产，但是他只有10000美元现金可用。他将这笔钱作为房地产保证金，并从银行借贷到剩余部分。因此，这位客户实际上拥有10000美元，或说该房地产的1%，而银行拥有99%。一年之后，该房地产价值为1010000美元。如果该客户使用自有财产支付全部采购价，那么他在这次投资中的获利为1%。但是，由于他只投入了10000美元，实际上他将自己的所有财产翻了一倍。他在该房地产中的10000美 元投资到如今已经又升值了10000美元，这意味着他获得了100%的利润。</p>
                    <p>这个相同的概念也适用于通过MSG迈盛进行的保证金投资。客户放下保证金以证明诚意，而MSG迈盛实际上则借给客户进行交易的资金。</p>
