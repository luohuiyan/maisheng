                    <p>例如，由MSG迈盛提供的原油合约潜在价值在客户赞成的情况下进行3%的活动，将会带来约为资产净值60%的利润。这种应用被称为杠杆作用(或齿轮传动效应)，是进行这些不稳定行情交易的关键。</p>
