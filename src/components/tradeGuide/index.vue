              <p><b>基准货币：</b>基准货币指货币对中的第一种货币，以及确定货币对价格时固定不变的货币。就外汇市场的日成交量而言，美元(USD)和欧元(EUR)是最主要的基准货币。 英镑(GBP)，又称sterling，是排名第三的基准货币。以美元为基准货币的货币对包括美元/日元、美元/瑞士法郎和美元/加元；以欧元为基准货币的货币对包括欧元/美元、欧元/日元、欧元/英镑和欧元/瑞士法郎。英镑是英镑/美元和英镑/日元货币对的基准货币。澳元(AUD)是澳元/美元(AUD/USD)货币对的基准货币。</p>
              <p><b>基准：</b>指现货价与期货价之间的差额。</p>
              <p><b>基点：</b>指一个百分点的百分之一。</p>
              <p><b>买卖价差：</b>指买价与卖价（出价）之间的差额，又称双向报价。</p>
              <p><b>央行：</b>指一个国家的主要货币管理机构，由中央政府控制，负责发行货币、制定货币政策、利率政策、汇率政策以及管理和监督私营银行部门。联邦储备局(Federal Reserve)是美国的央行。其它央行包括欧洲中央银行(European Central Bank)、英格兰银行(Bank of England)和日本中央银行(Bank of Japan)。</p>
              <p><b>兑换：</b>指将以一种货币计值的资产或负债转换为以另一种货币计值的资产或负债的过程。</p>
              <p><b>交叉汇率：</b>指两种货币之间的汇率。交叉汇率在货币对报价国未必是标准汇率。例如，在美国，英镑/瑞士法郎报价属于交叉汇率，而在英国或瑞士，它是主要货币对的汇率。</p>
              <p><b>货币：</b>指由国家政府或央行发行的交易单位，以货币价值作为交易的计量单位。</p>
              <p><b>货币（汇率）风险：</b>指因汇率不利变化而产生损失的风险。</p>
              <p><b>贬值：</b>指相对其它国家的货币，某个国家的货币价值下跌。当某个国家的货币贬值时，商品进口成本会上升，但出口成本会下降，可提高出口竞争力。</p>
              <p><b>跌幅：</b>指账户价值从最高值到最低值的下跌幅度，用百分比或金额表示。例如，如果交易者的账户价值从10,000美元增至20,000美元，又降至15,000美元，然后又增至25,000美元，则该交易者的最大跌幅是5,000美元（即账户从20,000美元跌至15,000美元），即使该交易者的账户自建仓来以来并未发生头寸损失也是如此。</p>
              <p><b>欧元：</b>指欧洲货币联盟（European Monetary Union，简称EMU）的货币，用于代替欧洲货币单位（European Currency Unit，简称ECU）。目前参与欧洲货币联盟的国家有奥地利、比利时、塞浦路斯、芬兰、法国、德国、希腊、爱尔兰、意大利、卢森堡、马耳他、荷兰、葡萄牙、斯洛文尼亚和西班牙。</p>
              <p><b>汇率：</b>指某种货币相对于其它货币的价格。例如：1加元(CDN) = 0.7700美元(USD)</p>
              <p><b>固定汇率：</b>指一个国家决定将该国货币的价值与其它国家的货币、黄金（或其它商品）或一篮子货币挂钩。但实际上，固定汇率也会在明确的上限和下限区间内浮动，从而需要干预。</p>
              <p><b>外汇(Forex)：</b>指在场外市场上同时买入某种货币和卖出另一种货币。</p>
              <p><b>G-7：</b>指七个主要的工业国家，分别是美国、德国、日本、法国、英国、加拿大和意大利。</p>
              <p><b>G-10：</b>指G7加上比利时、荷兰和瑞典，该国家集团主要参与国际货币基金组织(IMP)的磋商。瑞士有时也会参与。</p>
              <p><b>G-20：</b>指由下列20个国家的财政部长和央行行长组成的集团：阿根廷、澳大利亚、巴西、加拿大、中国、法国、德国、印度、印尼、意大利、日本、墨西哥、俄罗斯、沙特、南非、韩国、土耳其、英国、美国和欧盟。国际货币基金组织和世界银行也参与该集团。成立G-20的目的是通过制定"促进国际金融稳定性"的政策，以应对1997-99年的金融风暴。</p>
              <p><b>对冲基金：</b>指一种适合富裕投资者（最低投资额一般为1,000,000美元起）的无监管私营投资基金 ，主要从事对债券、货币、股票期权和衍生工具的短期高风险投机性投资。</p>
              <p><b>对冲：</b>指一种降低投资风险的策略，通过投资于替代投资工具，冲淡主要投资组合的风险，从而降低投资组合的波幅。</p>
              <p><b>伦敦银行同业拆借利率(LIBOR)：</b>指银行间拆放贷款的利率标准（通常以欧元计）。该利率适用于短期国际银行同业存款市场，并适用于期限为一天至五年的高额借款。该市场允许需要流动性的银行向有资金盈余的银行迅速借款，使银行避免持有过多的流动资产。伦敦银行同业拆借利率由少数伦敦大银行每天正式确定，但全天的利率可能发生变化。</p>
              <p><b>杠杆率：</b>指投资者或企业使用借款的比率。对投资者而言，杠杆率指凭保证金买入货币，以提高价值回报率，而不必增加投资额。该金额用倍数表示，交易的名义金额大于交易所需要的保证金。例如，如果交易的名义金额为100,000美元，而所需保证金为2,000美元，则交易者可采取50倍杠杆率（100,000美元/2,000美元）。采用杠杆进行投资的风险极高，因为您可能损失全部投资。</p>
              <p><b>流动性：</b>指市场接受大笔交易的能力。流动性因市场交易量和活跃程度而异，代表着买卖头寸和执行订单的效率与成本效益。市场流动性越强，报价越频繁，买卖价差越小。</p>
              <p><b>多头：</b>指买入特定货币而看空另一种货币，预期买入的货币升值，而另一种货币贬值。</p>
              <p><b>保证金：</b>指客户必须作为担保而存入的资金，用于弥补价格发生不利动向时产生的潜在损失。</p>
              <p><b>追缴保证金：</b>指经纪商或交易商要求存入额外资金或提供其它担保，以提高保证金的金额，为走势不利于客户的头寸提供必要的业绩担保。</p>
              <p><b>做市商：</b>指提供价格，并准备按此类买卖价格买入和卖出货币的交易商。有些在商品期货交易委员会注册的外汇交易商成员属于做市商。</p>
              <p><b>点（波动点）：</b>指货币市场上运用的一个述语，用于描述汇率的最低增幅移动。根据市场环境，正常情况下是一个基点。例如，欧元/美元、英磅/美元、 美元/瑞士法郎货币对中为 0.0001，而在美元/日元货币对中为 0.01。</p>
              <p><b>头寸：</b>指交易买入或卖出货币的意向，也指投资者拥有或借用的货币数量。</p>
              <p><b>升水（持仓成本）：</b>指将未平仓头寸从一个交割日延长到下一个交割日的成本或收益，按货币对中的两种货币的短期利差计算。</p>
              <p><b>重估：</b>指某种钉住其它货币或黄金的货币的外汇价值上升。</p>
              <p><b>重估汇率：</b>指任何时段或货币的汇率，用于重估头寸或账面价值。重估汇率也是交易者每天确定盈亏时使用的市场汇率。</p>
              <p><b>展期：</b>指将交易的交割延长到下一个交割日，其成本按两种货币的利差计算。又称隔夜掉期，特指下个营业日及其后营业日之间的利差。</p>
              <p><b>空头：</b>指并未实际拥有某种货币而将其卖出，持有空头者的往往期望价格下跌，然后再以更低价格买回获利。</p>
              <p><b>价差：</b>指货币买价与卖价（出价）之间的差额，用于衡量市场的流动性。价差越小，通常表明流动性越强。</p>
              <p><b>现货价：</b>指当前市价。现货交易一般在两个营业日内交割。</p>
              <p><b>掉期：</b>外汇掉期指在一笔交易中同时包含即期交易和远期交易，或两个到期日不同的远期交易。</p>
              <p><b>提价：</b>指相同货币的新报价高于上一个报价。</p>
              <p><b>外汇订单的类型</b></p>
              <p><b>挂单：</b>一种用于在货币对达到预定价位时执行交易的订单。</p>
              <p><b>进场限价单：</b>指要求未平仓头寸在行情上升时卖出货币，或在市情下跌时买入货币的订单。客户认为行情将在订单设定的价位发生反转。</p>
              <p><b>a. 买入限价单：</b>按低于当前市价买入货币的订单</p>
              <p><b>b. 卖出限价单：</b>按高于当前市价卖出货币的订单。</p>
              <p><b>进场止损单：</b>指要求未平仓头寸在行情下跌时卖出货币，或在市情上涨时买入货币的订单。客户相信价格触及订单价位后，会按之前动量的方向继续移动。</p>
              <p><b>a.买入止损单：</b>按高于当前市价买入货币的订单。</p>
              <p><b>b.卖出止损单：</b>按低于当前市价卖出货币的订单。</p>
              <p><b>限价单：</b>限价单是与特定头寸挂钩的订单，旨在锁定该头寸的盈利。在买入部位下的限价单表示卖出，而在卖出部位下的限价单表示买入。在客户平仓或取消限价单之前，所有限价单始终有效。</p>
              <p><b>市价单：</b>指按现行货币价格立即买入或卖出货币的订单。</p>
              <p><b>二选一单（One Cancels the Other，简称OCO）：</b>指将止损单与限价单同时与特定头寸挂钩的订单。其中止损单用于防止头寸发生额外损失，而限价单则为头寸创造盈利。执行任意一种订单，头寸将平仓，另一种订单将自动取消。</p>
              <p><b>止损单：</b>一种与特定头寸挂钩的订单，用于平仓止损。当GTS显示的价格触及止损价格时，则会执行止损单。执行价格为止损价格，如果遇到速动市场，则按下次显示的价格执行订单。在买入部位下的止损单表示在该部位卖出。而在卖出部位下的止损单表示在该部位买入。在客户平仓或取消止损单之前，所有止损单始终有效。</p>


            </div>

          </li>
        </ul>
      </div>

