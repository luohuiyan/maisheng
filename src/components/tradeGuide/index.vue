                    <p>基本走势线是制图者所采用的、最简单的技术工具之一，但是不管以何种标准衡量，都是交易中最有力、最有价值的工具。三个较高或较低的点连接起来即可构成走势线。这构成了监控价格变动的渠道。</p>
                    <p><b>绘制走势线</b></p>
                    <p>如制图其它方面一样，正确绘制走势线是一门技术。为找到正确的线，需要对不同的线做一些试验。有时，看上去似乎正确的走势线也需要重新绘制。多加练习，绘制走势线也会变得更容易，但最开始，一些指导可以帮助您找到正确的线。 </p>
                    <p>走势必须是显着的。这意味着，对于绘制上涨走势线而言，必须至少有两个下跌点，第二个低价点要比第一个低价点高些。一旦识别出两个处于上升之中的低点，即可绘制出连接低价点的直线，并向上和向右投射出。 </p>
                    <p> 一旦确定第三点且该走势继续按照原方向进行下去，那么该走势线将在多个方面变得非常有用。技术性分析的基本观念之一就是，动态的走势往往保持一定的运动方向。因此，一旦走势呈现特定的斜率或者速率，如根据走势线确定的那样，那么它通常会保持相同的斜率。该走势线不仅可以用来判定可矫正期的末端，而且更重要的是，能够判定该走势何时发生变化。走势线的突然中断往往就是走势变化的前兆。</p>
                    <p><b>走势线的重要性</b></p>
                    <p>讨论如何确定走势线的意义是极其重要的。在每个市场以及您所看到的每份图表，存在许多动态的趋势，有短期的、中期的、长期的以及小时的等等。但并非所有这些走势都有实际意义。若它们没有实际意义，交易者会在错误的时间冒着风险进出市场。走势线越显着，就会激发股民更多的信心，其渗透性也就越重要。有两个因素可以确定走势线的显着性。第一，未受影响的时间长度；第二，测试次数。例如，经市场检验八次的走势线，但不考虑价格，显然比仅经两次检验的走势线更加重要。第三次自走势线反弹之后粗略估价的时候正是市场开始发出交易信号之时。类似的情况，在过去九个月内未受影响的走势线也会比在过去九个星期内未受影响的走势线更重要。在依赖于走势之前，要等待多久是没有标准的，因为有些走势变动仅仅是短期的。为掌握这些内容，您不得不利用与走势线检验相关联的时间。</p>
